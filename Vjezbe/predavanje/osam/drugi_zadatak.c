int makeMatrix()
{
    printf("{");
    for (int z = 0; z < 4; z++)
    {
        printf("{");

        for (int y = 0; y < 3; y++)
        {
            printf("{");
            for (int x = 0; x < 6; x++)
            {
                printf("%d, ", (z + 1) * 100 + (y + 1) * 10 + x + 1);
            }
            if (y != 2)
                printf("},");
            else
                printf("}");
        }
        if (z != 3)
            printf("},");
        else
            printf("}");
    }
    printf("}");

    return 0;
}

